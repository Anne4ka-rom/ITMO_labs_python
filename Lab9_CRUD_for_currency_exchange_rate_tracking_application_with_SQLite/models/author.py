class Author:
    '''
    Класс Author представляет имя и группу автора приложения
    Обеспечивает валидацию данных при установке свойств
    '''
    def __init__(self, name: str, group: str):
        '''
        Функция __init__() инициализирует новый экземпляр класса Author
        
        Параметры:
        name -- имя автора
        group -- группа автора
        
        Вызывает:
        TypeError -- если name или group не строки
        ValueError -- если name или group пустые строки после удаления пробелов
        '''
        self.__name = None # устанавливаем начальное значение приватного атрибута __name как None
        self.__group = None # устанавливаем начальное значение приватного атрибута __group как None
        self.name = name # вызываем сеттер name для установки имени с валидацией
        self.group = group # вызываем сеттер group для установки группы с валидацией

    @property # декоратор @property для создания свойства
    def name(self) -> str:
        '''
        Функция name() возвращает имя автора в строковом виде
        '''
        return self.__name # возвращаем значение приватного атрибута __name
    
    @name.setter # декоратор setter для свойства name
    def name(self, value: str):
        '''
        Функция name() устанавливает имя автора с валидацией
        
        Параметры:
        value -- новое имя автора
        
        Вызывает:
        TypeError -- если значение не является строкой
        ValueError -- если строка пустая после удаления пробелов
        '''
        if not isinstance(value, str): # проверяем, является ли значение чем-то кроме строки
            raise TypeError('Имя должно быть строкой') # вызываем исключение TypeError
        if len(value.strip()) == 0: # проверяем, являетсяя ли строкапустой после удаления пробелов
            raise ValueError('Имя не может быть пустым') # вызываем исключение ValueError
        self.__name = value.strip() # устанавливаем значение приватного атрибута __name, удаляя лишние пробелы
    
    @property # декоратор @property для создания свойства
    def group(self) -> str:
        '''
        Функция group() возвращает группу автора в строковом виде
        '''
        return self.__group # возвращаем значение приватного атрибута __group
    
    @group.setter # декоратор setter для свойства group
    def group(self, value: str):
        '''
        Функция group() устанавливает группу автора с валидацией
        
        Параметры:
        value -- новая группа автора
        
        Вызывает:
        TypeError -- если значение не является строкой
        ValueError -- если строка пустая после удаления пробелов
        '''
        if not isinstance(value, str): # проверяем, является ли значение чем-то кроме строки
            raise TypeError('Группа должна быть строкой') # вызываем исключение TypeError
        if len(value.strip()) == 0: # проверяем, является ли строка пустой после удаления пробелов
            raise ValueError('Группа не может быть пустой') # вызываем исключение ValueError
        self.__group = value.strip() # устанавливаем значение приватного атрибута __group, удаляя лишние пробелы
    
    def __repr__(self):
        '''
        Функция __repr__() возвращает строковое представление объекта для отладки
        '''
        return f"Author(name='{self.name}', group='{self.group}')" # возвращаем строку с информацией об объекте Author