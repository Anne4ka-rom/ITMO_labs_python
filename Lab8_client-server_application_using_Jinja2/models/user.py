class User:
    '''
    Класс User представляет пользователя приложения
    Инкапсулирует данные о пользователе и обеспечивает их валидацию
    '''
    def __init__(self, id: int, name: str):
        '''
        Функция __init__() инициализирует новый экземпляр класса User
        
        Параметры:
        id -- уникальный идентификатор пользователя
        name -- имя пользователя
        
        Вызывает:
        TypeError -- если id не целое число
        ValueError -- если id не положительное число
        TypeError -- если name не строка
        ValueError -- если name пустая строка после удаления пробелов
        '''
        self._id = None # создаём защищённый атрибут для идентификатора пользователя со значением None
        self._name = None # создаём защищённый атрибут для имени пользователя со значением None
        self.id = id # устанавливаем публичное свойство id через сеттер
        self.name = name # устанавливаем публичное свойство name через сеттер
    
    @property # декоратор для создания свойства
    def id(self) -> int:
        '''
        Функция id() возвращает уникальный идентификатор пользователя в виде целого числа
        '''
        return self._id # возвращаем значение защищённого атрибута _id
    
    @id.setter # декоратор, преобразующий метод id() в сеттер для свойства id
    def id(self, value: int):
        '''
        Функция id() устанавливает уникальный идентификатор пользователя с валидацией
        
        Параметры:
        value -- новый уникальный идентификатор пользователя
        
        Вызывает:
        TypeError -- если значение не является целым числом
        ValueError -- если значение не является положительным числом
        '''
        if not isinstance(value, int): # проверяем, является ли значение чем-то кроме целого числа
            raise TypeError("ID должен быть целым числом") # вызываем исключение TypeError для неверного типа
        if value <= 0: # проверяем, является ли значение неположительным числом
            raise ValueError("ID должен быть положительным числом") # вызываем исключение ValueError для неположительного числа
        self._id = value # сохраняем значение в защищённый атрибут _id
    
    @property # декоратор для создания свойства
    def name(self) -> str:
        '''
        Функция name() возвращает имя пользователя в строковом виде
        '''
        return self._name # возвращаем значение защищённого атрибута _name
    
    @name.setter # декоратор, преобразующий метод name() в сеттер для свойства name
    def name(self, value: str):
        '''
        Функция name() устанавливает имя пользователя с валидацией
        
        Параметры:
        value -- новое имя пользователя
        
        Вызывает:
        TypeError -- если значение не является строкой
        ValueError -- если строка пустая после удаления пробелов
        '''
        if not isinstance(value, str): # проверяем, является ли значение чем-то кроме строки
            raise TypeError("Имя пользователя должно быть строкой") # вызываем исключение TypeError для неверного типа
        if len(value.strip()) == 0: # проверяем, пустая ли строка после удаления пробелов
            raise ValueError("Имя пользователя не может быть пустым") # вызываем исключение ValueError для пустой строки
        self._name = value.strip() # сохраняем очищенное значение в защищённый атрибут _name
    
    def __repr__(self):
        '''
        Функция __repr__() возвращает строковое представление объекта для отладки
        '''
        return f"User(id={self.id}, name='{self.name}')" # возвращаем форматированную строку с информацией об объекте